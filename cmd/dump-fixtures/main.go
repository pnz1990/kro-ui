// withGroup sets the API group on the schema so generated CRDs land in
// the e2e group instead of the default kro.run group.
func withGroup(g string) generator.ResourceGraphDefinitionOption {
	return func(rgd *krov1alpha1.ResourceGraphDefinition) {
		if rgd.Spec.Schema != nil {
			rgd.Spec.Schema.Group = g
		}
	}
}

// writeFixture marshals obj to YAML, prepends the Apache 2.0 header, and
// writes to path. Overwrites existing files (idempotent).
// If obj is an *krov1alpha1.ResourceGraphDefinition, TypeMeta is injected
// before serialisation so kubectl apply works without a separate apiVersion/kind.
func writeFixture(path string, obj interface{}) error {
	if rgd, ok := obj.(*krov1alpha1.ResourceGraphDefinition); ok {
		rgd.TypeMeta = metav1.TypeMeta{
			APIVersion: krov1alpha1.KRODomainName + "/v1alpha1",
			Kind:       "ResourceGraphDefinition",
		}
	}
	raw, err := yaml.Marshal(obj)
	if err != nil {
		return fmt.Errorf("marshal %s: %w", path, err)
	}
	content := apacheHeader + string(raw)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		return fmt.Errorf("write %s: %w", path, err)
	}
	return nil
}

// writeMultiDocFixture writes multiple objects to a single YAML file
// separated by "---" document markers.
func writeMultiDocFixture(path string, objs ...interface{}) error {
	var sb strings.Builder
	sb.WriteString(apacheHeader)
	for i, obj := range objs {
		if i > 0 {
			sb.WriteString("---\n")
		}
		raw, err := yaml.Marshal(obj)
		if err != nil {
			return fmt.Errorf("marshal doc %d of %s: %w", i, path, err)
		}
		sb.WriteString(string(raw))
	}
	if err := os.WriteFile(path, []byte(sb.String()), 0o644); err != nil {
		return fmt.Errorf("write %s: %w", path, err)
	}
	return nil
}

// instanceObject builds a bare unstructured CR object.
func instanceObject(kind, name, ns string, spec map[string]interface{}) *unstructured.Unstructured {
	return &unstructured.Unstructured{
		Object: map[string]interface{}{
			"apiVersion": group + "/" + apiVersion,
			"kind":       kind,
			"metadata": map[string]interface{}{
				"name":      name,
				"namespace": ns,
			},
			"spec": spec,
		},
	}
}


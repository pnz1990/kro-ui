// ── main ──────────────────────────────────────────────────────────────────────

func main() {
	dir := fixturesDir
	if len(os.Args) > 1 {
		dir = os.Args[1]
	}

	type job struct {
		name string
		fn   func() error
	}

	jobs := []job{
		{"upstream-cartesian-foreach-rgd.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-cartesian-foreach-rgd.yaml"), buildCartesianForEachRGD())
		}},
		{"upstream-cartesian-foreach-instance.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-cartesian-foreach-instance.yaml"), buildCartesianForEachInstance())
		}},
		{"upstream-collection-chain-rgd.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-collection-chain-rgd.yaml"), buildCollectionChainRGD())
		}},
		{"upstream-collection-chain-instance.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-collection-chain-instance.yaml"), buildCollectionChainInstance())
		}},
		{"upstream-contagious-include-when-rgd.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-contagious-include-when-rgd.yaml"), buildContagiousIncludeWhenRGD())
		}},
		{"upstream-contagious-include-when-instance.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-contagious-include-when-instance.yaml"), buildContagiousIncludeWhenInstance())
		}},
		{"upstream-cluster-scoped-rgd.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-cluster-scoped-rgd.yaml"), buildClusterScopedRGD())
		}},
		{"upstream-external-collection-rgd.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-external-collection-rgd.yaml"), buildExternalCollectionRGD())
		}},
		{"upstream-external-collection-prereq.yaml", func() error {
			prereqs := buildExternalCollectionPrereq()
			objs := make([]interface{}, len(prereqs))
			for i := range prereqs {
				objs[i] = prereqs[i]
			}
			return writeMultiDocFixture(filepath.Join(dir, "upstream-external-collection-prereq.yaml"), objs...)
		}},
		{"upstream-external-collection-instance.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-external-collection-instance.yaml"), buildExternalCollectionInstance())
		}},
		{"upstream-cel-comprehensions-rgd.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-cel-comprehensions-rgd.yaml"), buildCelComprehensionsRGD())
		}},
		{"upstream-cel-comprehensions-instance.yaml", func() error {
			return writeFixture(filepath.Join(dir, "upstream-cel-comprehensions-instance.yaml"), buildCelComprehensionsInstance())
		}},
	}

	var errs []string
	for _, j := range jobs {
		if err := j.fn(); err != nil {
			errs = append(errs, fmt.Sprintf("  %s: %v", j.name, err))
		} else {
			fmt.Printf("  wrote %s\n", j.name)
		}
	}

	if len(errs) > 0 {
		fmt.Fprintf(os.Stderr, "dump-fixtures: %d error(s):\n%s\n", len(errs), strings.Join(errs, "\n"))
		os.Exit(1)
	}
	fmt.Printf("dump-fixtures: wrote %d files to %s\n", len(jobs), dir)
}
